  <p style="margin-top : 4%"> 
        <strong>Bedoyo (SID)</strong>,  Ratusan peserta yang berasal dari kelompok senam ibu-ibu, Pamong Bedoyo dan Puskesmas  Ponjong II hadiri Kampanye Germas (Gerakan Masyarakat Hidup Sehat) di halaman Rest area Bedoyo, Sabtu 23 Juli 2022. Meski dilangsungkan dalam masa kesibukan warga punya hajatan namun tidak mengurangi antusiasme warga terutama ibu-ibu untuk hadir ber senam ria bersama. Acara serupa pernah digelar ditempat yang sama  namun semenjak ada Pandemi sempat 0ff dua tahun.

Diawali dengan sambutan Lurah Bedoyo yang mengapresiasi kegiatan dari Puskesmas Ponjong II terkait Germas ini juga beryterimakasih seluruh peserta senam yang hadir di acara ini. “ semoga acara serupa bisa digelar lagi, misalnya dengan  durasi tiap minggu atau bulanan, tentu bisa hidup sehat bugar dan kita undang para pelaku UKM untuk menjajakan daganganya”.

Dari puskesmas Ponjong II diwakili bapak Kepala TU Puskesmas II menyampaikan terimakasih atas peran serta semua pihak dengan terselenggaranya acara ini.

Usai senam dilanjutkan materi tentang Germas oleh Dokter Jazuli yang menekankan pentingnya perilaku hidup sehat dengan tagline CERDIK yaitu :</p>

<strong>Apa Itu CERDIK?</strong></p>

CERDIK merupakan perilaku hidup sehat yang mampu menjauhkan Anda dari berbagai penyakit tidak menular (PTM) seperti penyakit pembuluh darah, jantung, hingga masalah ginjal. Apa saja perilaku yang termasuk CERDIK?</p>

<strong>1. Cek Kesehatan Secara Berkala</strong></p>
Banyak masyarakat Indonesia yang masih mengabaikan cek kesehatan secara berkala. Padahal langkah ini bisa membantu masyarakat mendeteksi penyakit-penyakit dalam sejak dini. Mulailah memonitor tekanan darah, menimbang berat badan, mengukur tinggi badan, mengukur lingkar perut, dan perhatikan denyut nadi Anda. Jangan lupa pula mengecek kadar kolesterol dan gula darah secara teratur. 

Enyahkan Asap Rokok
Tentu Anda sudah tahu kalau merokok bisa berdampak buruk bagi kesehatan bukan hanya bagi diri sendiri, tapi juga orang-orang di sekitar Anda. Dampak rokok juga bukan hanya pada sektor kesehatan, tapi juga keuangan. Tak ada salahnya bila mulai saat ini Anda berhenti merokok demi kehidupan yang lebih baik.</p>

<strong>2. Rajin Aktivitas Fisik/Olahraga</strong></p>

Guna menjaga kesehatan dan mencegah penyakit kardiovaskuler, berolahragalah secara rutin setidaknya minimal selama 30 menit per hari sebanyak 3-5 kali per minggu.</p>

<strong>3.Diet Sehat dan Seimbang</strong></p>

<p >Imbangi aktivitas olahraga dengan melakukan diet sehat dan seimbang yakni mengkonsumsi buah dan sayur 5 porsi per hari. Batasi konsumsi gula tak lebih dari 4 sendok makan per hari per orang dan garam tak lebih dari 1 sendok teh per orang per hari. Batasi pula konsumsi lemak (GGL) atau minyak tak lebih dari 5 sendok makan per hari per orang. 

Bagi Anda yang menyukai makanan manis, sebaiknya mulai mengurangi makanan dengan kandungan gula tinggi seperti soft drink, permen, kue basah, kue kering dan es krim. Kurangi pula konsumsi gula putih atau gula merah, sirup serta madu. Gantikan makanan manis tersebut dengan buah segar maupun minuman jus buah segar kesukaan Anda. 

Untuk menjaga kesehatan, mau tak mau Anda harus rajin membaca label kemasan makanan sebelum membeli. Kurangi makanan dan minuman yang mengandung gula tersembunyi seperti maltosa, glukosa, sukrosa, laktosa, dekstrosa, fruktosa dan sirup. Batasi konsumsi makanan dengan kandungan garam tinggi seperti keju, buah kering, makanan kemasan, kacang asin dan keripik kentang. 

Tak ketinggalan kurangi pula konsumsi lemak dengan memilih makanan sumber protein seperti daging tanpa lemak,kacang kering, unggas, ikan, dan kacang polong. Kurangi konsumsi daging merah dan buang lemak di daging sebelum dimasak. Bila ingin minum susu, pilih susu rendah lemak dan hindari jeroan serta kurangi makan telur.</p>

<strong>4.Istirahat Cukup</strong></p>

Bagi orang dewasa, istirahatlah yang cukup dengan tidur selama 7-8 jam sehari.</p>

<strong>5.Kelola Stres</strong></p> 

Terakhir, kurangi potensi penyakit kardiovaskuler dengan mengelola stres. Sering-seringlah rekreasi, relaksasi, berpikiran positif dan bercengkrama dengan orang lain. Terapkan pola hidup teratur dan rencanakan masa depan Anda sebaik-baiknya.

Acara terakhir yaitu screening kesehatan untuk mengukur kondisi tubuh memonitor tekanan darah, menimbang berat badan, mengukur tinggi badan, mengukur lingkar perut, dan perhatikan denyut nadi Anda, mengukur volume CO2 paru-paru.

