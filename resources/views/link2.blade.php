  <p style="margin-top : 4%"> 
  <strong>Bedoyo (SID)</strong>, Tanggal 19 juli 2022 bertempat di Balai Kalurahan Bedoyo dilangsungkan Sosialisasi GISA dari Dinas DUKCAPIL Kabupaten Gunungkidul.Dari Dinas diwakili oleh Kabid PIAK ibu Rohmi Rahayu bersama jajaranya dan diterima oleh Carik Bedoyo.</p>

Sambutan Lurah Bedoyo disampaikan oleh Carik Bedoyo yang memohonkan ijin Lurah Bedoyo karna sedang mengikuti sosialisasi Pergub DIY di Kabupaten. Supanto memberikan ucapan selamat dating dan terimakasih kepada Dukcapil yang sudah berkenan rawuh untuk menyampaikan sosialisasi  terkait AMINDUK. Pemkal Bedoyo hadirkan peserta dari beberapa unsur, Pamong, Bamuskal, PKK, RT dan Tokmas.  Kepada peserta yang hadir dimohon untuk menyampaikan informasi terkait sosialisasi ini ke semua warga.</p>

Dalam penyampaian materinya ibu Rohmi menekankan tentang adanya GISA atau Gerakan Indonesia Sadar Adminduk.</p>

GISA atau Gerakan Indonesia Sadar Adminduk adalah sebuah gerakan nasional untuk meningkatkan kesadaran semua pihak (masyarakat, lembaga pengguna, dan pemerintah) akan pentingnya tertib Administrasi Kependudukan (Adminduk), papar ibu Rohmi yang lulusan Universiatas ternama Universitas Gajah Mada. Ibu Rohmi berharap nanti lebih banya diskusi sesuai dengan beberapa kejadian/ masalah aminduk di Bedoyo agar ada solusi. “untuk memudahkan dan mempercepat pelayanan mohon Desa segera mengajukan permohonan kerjasama, sehingga pelayanan untuk yang sederhana bisa selesai di Desa”, pinta bu Rohmi.</p>

Session selanjutnya lebih banyak diskusi dan pertanyaan dari pesrta kepada Nara sumber. Acara ditutup dengan ucapan terimakasih dari Lurah Bedoyo, Bapak Sulardi SPd, MPd yang di penghujung acara ini ternyata bisa hadir.<strong>(Pak Carik)</strong>
