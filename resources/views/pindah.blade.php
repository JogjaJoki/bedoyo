                    <h4><span style="color:#FF0000;"><strong>Syarat dan Cara Membuat Surat Pindah Domisili</strong></span></h4>
<p><span data-cke-marker="1">&nbsp;&nbsp;</span></p>
<p>Berikut ini syarat yang harus disiapkan untuk membuat surat pindah domisili:</p>
<ul>
  <li>Pertama sebelum kamu benar-benar membuat surat pindah domisili adalah mempunyai alamat tujuan pindah</a></li>
  <li>Kartu Keluarga (KK) asli dan fotokopi</li>
  <li>KTP asli dan fotokopi</li>
  <li>Siapkan pas foto 3 – 5 lembar ukuran 3×4</li>
</ul>

<p>Kamu harus tahu lingkup mana saja yang digolongkan kedalam kepengurusan surat pindah domisili:</p>

<p><strong style="color:#FF0000;">1. Pindah RT / RW dalam Satu Desa / Kelurahan</strong></p>

<p>Jika kamu pindah alamat masih didalam satu kelurahan / desa, dalam hal ini pindah beda RT atau RW, maka cara membuat surat pindah domisili cukup dikeluarkan dan ditandatangani oleh Aparat Desa / Kepala Desa setempat.</p>

<p>Adapun cara membuat sebagai berikut:</p>

<ul>
  <li>Membuat surat pengantar yang ditandatangani oleh aparat RT dan RW</li>
  <li>Surat pengantar dan dokumen lainnya diserahkan ke kantor Desa / Kelurahan untuk dibuatkan surat pindah domisili oleh Kepala Desa</li>
  <li>Tahap selanjutnya kamu kekantor Kecamatan untuk mengurus KK baru (alamat lama) untuk proses pencoretan / menghapus nama pemohon dari KK lama, proses ini selesai dan Surat Pindah Domisili dari Kelurahan bisa kamu gunakan untuk mengurus KK baru dengan alamat baru.</li>
</ul>
<p><strong style="color:#FF0000;">2. Pindah Domisili Satu Kecamatan</strong></p>

<p>Apabila kamu pindah alamat masih dalam satu kecamatan beda Desa, maka kamu akan membuat surat pindah domisili yang dikeluarkan dan ditandatangani oleh Aparat Desa / Kepala Desa.</p>

<p>Cara membuat surat pindah domisili pada golongan ini <strong>sama</strong> dengan tahapan Pindah Domisili RT / RW dalam satu Kelurahan / Desa.</p>
<p><strong style="color:#FF0000;">3. Pindah Domisili antar Kecamatan dalam satu Kabupaten</strong></p>

<p>Jika kamu ingin membuat surat pindah dalam lingkup ini, kamu akan mengurus sampai ditandatangani dan dikeluarkan oleh Camat / Kepala Dinas Kecamatan.</p>

<p>Adapun cara membuat nya sebagai berikut:</p>

<ul>
  <li>Membuat surat pengantar yang ditandatangani oleh aparat RT dan RW</li>
  <li>Surat pengantar dan syarat lengkap diserahkan ke kantor Desa / Kelurahan untuk dibuatkan surat pengantar pindah domisili dari Kepala Desa / Aparat Desa</li>
  <li>Bawa surat pengantar dari Desa berikut syarat yang lain ke kantor Kecamatan untuk dibuatkan Surat Pindah Domisili dari Camat</li>
  <li>Masih pada kantor Kecamatan, kamu sekalian mengurus KK baru / cetak baru (alamat lama) untuk proses pencoretan / menghapus nama pemohon dari KK lama, pada proses ini telah selesai dan Surat Pindah Domisili dari Kecamatan bisa kamu gunakan untuk mengurus KK baru dengan alamat baru.</li>
</ul>
<p><strong style="color:#FF0000;">4. Pindah Domisili keluar Kota / Kabupaten dalam satu Provinsi</strong></p>

<p>Maka surat pindah alamat dikeluarkan dan ditandatangani oleh Kepala Dinas Kependudukan dan Pencatatan Sipil (Dispendukcapil) dikabupaten / kota setempat.</p>

<p>Berikut cara membuat Surat Pindah Domisili:</p>

<ul>
  <li>Membuat surat pengantar yang ditandatangani oleh aparat RT dan RW</li>
  <li>Surat pengantar dan syarat lengkap diserahkan ke kantor Desa / Kelurahan untuk dibuatkan surat pengantar pindah domisili dari Kepala Desa / Aparat Desa</li>
  <li>Bawa surat pengantar dari Desa berikut syarat yang lain ke kantor Kecamatan untuk dibuatkan surat pengantar pindah domisili dari Camat</li>
  <li>Surat pengantar dari Camat dan syarat lengkap lainnya diserahkan kekantor Dispendukcapil kabupaten / Kota setempat untuk proses pembuatan surat pindah domisili. Pada tahapan ini biasanya memerlukan waktu 12 hari kerja dan KTP asli yang lama akan ditarik oleh pihak Dinas Dispendukcapil.</li>
</ul>

<p><strong style="color:#FF0000;">5. Pindah Domisili antar Provinsi</strong></p>

<p>Surat pindah alamat / cabut berkas dibuat dan ditandatangai sampai pada Kepala Dinas kependudukan dan Pencatatan Sipil (Dukcapil) kabupaten / kota setempat.</p>

<p>Cara kepengurusan pada golongan ini sama dengan Pindah keluar Kota / Kabupaten</p>

<p><strong style="color:#FF0000;">6. Pindah Tempat Tinggal Keluar Negeri</strong></p>

<p>Dan jika kamu ingin mengurus surat pindah domisili keluar Negeri kamu bisa mengurus sampai kantor Dispendukcapil wilayah kabupaten / kota setempat seperti tahapan Pindah Domisili antar Provinsi dan dengan melengkapi persyaratan seperti di web pemerintahan</strong></a> ini.</p>

